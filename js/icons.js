function icon(name, size) {
  size = size || 20;
  const body = FEATHER_ICONS[name];
  if (!body) return '';
  return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${body}</svg>`;
}

/* Replaces <i data-icon="name" data-size="18"></i> placeholders with real SVG markup. Call after injecting HTML. */
function hydrateIcons(root) {
  (root || document).querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.getAttribute('data-icon');
    const size = el.getAttribute('data-size') || 20;
    el.innerHTML = icon(name, size);
  });
}
